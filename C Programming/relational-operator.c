
#include <stdlib.h>
#include <stdio.h>

/* Relational operator */

int main(int argc, char **argv)
{
int a = 1;

//Equal- relational operator
if(a==1)
{
        printf("\"==\" a variable is equal 1\n");
}

//Not equal- relational operator
if(a!=0)
{
        printf("\"!=\" a variable is not equal 0 \n");
}

//Greater than- relational operator
if(a>0)
{
        printf("\">\" a variable is greater than 0\n");
}

//Less than- relational operator
if(a<2)
{
        printf("\"<\" a variable is less than 2\n");
}

//Greater than equal- relational operator
if(a<=1)
{
        printf("\"<=\" a variable is less than equal 1\n");
}

//Less than equal- relational operator 
if(a>=1)
{
        printf("\">=\" a variable is greater than equal 1\n");
}





return 0;
}
